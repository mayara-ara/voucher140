def soma (n1,n2):
    s = n1 + n2
    return s

def mult(n1,n2):
    s = n1 * n2
    return s

def div (n1,n2):
    s = n1 / n2
    return s

def subtracao(n1,n2):
    s = n1 - n2
    return s

def pot(n1,n2):
    s = n1 ** n2
    return s

x = soma(10,20)
y = mult(10,20)
z = pot(3,4)

print(x + y + z)